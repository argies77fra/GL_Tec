package com.example.siriaco77.qpm.Componentes.Etapa_Produccion;

public class Etapa_Produccion {

    String Id;
    String Descripcion;
    String Nombre;
    String NumeroOrden;
    String IdTipoColmena;
    String CantidadDiasEtapa;

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String descripcion) {
        Descripcion = descripcion;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public String getNumeroOrden() {
        return NumeroOrden;
    }

    public void setNumeroOrden(String numeroOrden) {
        NumeroOrden = numeroOrden;
    }

    public String getIdTipoColmena() {
        return IdTipoColmena;
    }

    public void setIdTipoColmena(String idTipoColmena) {
        IdTipoColmena = idTipoColmena;
    }

    public String getCantidadDiasEtapa() {
        return CantidadDiasEtapa;
    }

    public void setCantidadDiasEtapa(String cantidadDiasEtapa) {
        CantidadDiasEtapa = cantidadDiasEtapa;
    }




}
